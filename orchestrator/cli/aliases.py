"""Terminal launcher installer for the public Symbiont CLI commands.

Usage:
    make aliases            # installs @ and symbiont

After installation, only the public terminal commands are installed in PATH:

    @ olá!                                # → curl POST /query → streaming
    @ corrige este bug                    # → curl POST /query → streaming
    cat file.py | @ analisa este ficheiro # → stdin included in query
    symbiont                              # → Rust terminal chat UX
    symbiont -l                           # → Rust sessions/tasks UX

Scripts call the symbiont API via curl (requires symbiont running via ``make infra``).
Works on any OS, any shell — no .zshrc, .bashrc, or venv needed.
"""

