def _install_render_script(bin_dir: Path) -> None:
    """Install the private streaming Markdown→ANSI helper."""
    render_src = Path(__file__).resolve().parent / "render.py"
    render_dst = _user_lib_dir() / "orc-render"

    if render_src.exists():
        _atomic_copy_executable(render_src, render_dst)


def _atomic_copy_executable(src: Path, dst: Path) -> None:
    """Install an executable without rewriting a running binary in place."""
    import shutil

    dst.parent.mkdir(parents=True, exist_ok=True)
    tmp = dst.with_name(f".{dst.name}.tmp-{os.getpid()}")
    try:
        shutil.copy2(src, tmp)
        tmp.chmod(tmp.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
        tmp.replace(dst)
    finally:
        if tmp.exists():
            tmp.unlink()


def _install_symbiont_tui_binary(bin_dir: Path) -> bool:
    """Install the compiled Rust Symbiont terminal binary when available."""
    crate_dir = _symbiont_tui_crate_dir()
    installed = False
    for candidate in (
        crate_dir / "target" / "release" / "symbiont-tui",
        crate_dir / "target" / "debug" / "symbiont-tui",
    ):
        if candidate.exists():
            tui_dst = _user_lib_dir() / "symbiont-tui"
            _atomic_copy_executable(candidate, tui_dst)
            installed = True
            break

    for old_binary in (bin_dir / "symbiont-tui",):
        if old_binary.exists():
            old_binary.unlink()
    return installed


def install_aliases() -> dict[str, Path]:
    """Install the generated public terminal commands into ~/.local/bin/.

    The ``@`` compatibility alias is pure bash/curl. The primary ``symbiont``
    terminal launcher uses the private installed Rust TUI binary.
    Requires the symbiont API to be running (``make infra``).

    Returns dict of alias → script path for successfully installed aliases.
    """
    # Public CLI policy: keep exactly @ and symbiont as user-facing commands.
    # Registry aliases remain model metadata; they no longer become PATH commands.
    alias = "@"

    # Session continuity settings are sourced from [session] without loading
    # the full runtime config, so alias installation does not depend on LLM secrets.
    cli_default_session, session_state_file = _load_cli_session_settings()

    aliases = [alias]
    desired_aliases = set(aliases)

    bin_dir = _user_bin_dir()
    _install_render_script(bin_dir)
    tui_installed = _install_symbiont_tui_binary(bin_dir)
    if not tui_installed:
        click.echo(
            click.style(
                "  ⚠ symbiont-tui binary not found — run `make build-symbiont-tui` before using `symbiont`",
                fg="yellow",
            ),
            err=True,
        )
    installed: dict[str, Path] = {}

    for path in sorted(bin_dir.iterdir()):
        if not path.is_file() or path.name in desired_aliases:
            continue
        try:
            content = path.read_text()
        except (OSError, UnicodeDecodeError):
            continue
        if _is_generated_alias_script(content):
            path.unlink()

    for alias in sorted(aliases):
        # Never shadow reserved system commands (e.g. VS Code's `code`)
        if alias in _RESERVED_NAMES:
            click.echo(
                click.style(f"  ⚠ Skipping '{alias}' — reserved system command name", fg="yellow"),
                err=True,
            )
            continue

        script_path = bin_dir / alias

        # Don't overwrite non-alias files (safety check)
        if script_path.exists():
            try:
                content = script_path.read_text()
                if not _is_generated_alias_script(content):
                    click.echo(
                        click.style(f"  ⚠ Skipping '{alias}' — file exists and is not an alias script", fg="yellow"),
                        err=True,
                    )
                    continue
            except (OSError, UnicodeDecodeError):
                continue

        script_content = _build_script(
            alias,
            _SECRET_FILE,
            cli_default_session=cli_default_session,
            session_state_file=session_state_file,
        )
        script_path.write_text(script_content)
        script_path.chmod(script_path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
        installed[alias] = script_path

    symbiont_path = bin_dir / "symbiont"
    if symbiont_path.exists():
        try:
            content = symbiont_path.read_text()
            can_overwrite = _is_generated_alias_script(content)
        except (OSError, UnicodeDecodeError):
            can_overwrite = False
    else:
        can_overwrite = True
    if can_overwrite:
        symbiont_path.write_text(_build_symbiont_launcher())
        symbiont_path.chmod(symbiont_path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
        installed["symbiont"] = symbiont_path
    else:
        click.echo(
            click.style("  ⚠ Skipping 'symbiont' — file exists and is not an ai-local generated script", fg="yellow"),
            err=True,
        )

    for alias in installed:
        _install_zsh_noglob_alias(alias)

    return installed


def remove_aliases() -> list[str]:
    """Remove all auto-generated alias scripts from ~/.local/bin/.

    Returns list of removed alias names.
    """
    bin_dir = _user_bin_dir()
    removed: list[str] = []

    for path in sorted(bin_dir.iterdir()):
        if not path.is_file():
            continue
        try:
            content = path.read_text()
            if _is_generated_alias_script(content):
                path.unlink()
                removed.append(path.name)
        except (OSError, UnicodeDecodeError):
            continue

    return removed
